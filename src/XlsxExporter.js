/**
 * Create a workbook from multiple sheets and trigger download.
 * @param {string} filename
 * @param {Array<{name:string, data:Array<Array>}>} sheets
 */
async function writeWorkbook(filename, sheets) {
  const XLSX = await import("xlsx");
  const wb = XLSX.utils.book_new();
  sheets.forEach(s => {
    const ws = XLSX.utils.aoa_to_sheet(s.data);
    // Auto-width columns based on content
    const colWidths = [];
    s.data.forEach(row => {
      row.forEach((cell, ci) => {
        const len = String(cell || "").length;
        colWidths[ci] = Math.max(colWidths[ci] || 8, Math.min(len + 2, 50));
      });
    });
    ws["!cols"] = colWidths.map(w => ({ wch: w }));
    XLSX.utils.book_append_sheet(wb, ws, s.name.slice(0, 31)); // Excel 31-char sheet name limit
  });
  const wbout = XLSX.write(wb, { bookType: "xlsx", type: "array" });
  const blob = new Blob([wbout], { type: "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet" });
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = filename;
  a.click();
  URL.revokeObjectURL(a.href);
}

/**
 * Build standard cover sheet data.
 */
function coverSheet(ctx, wp) {
  return {
    name: "Cover",
    data: [
      ["AUDIT WORKING PAPER"],
      [],
      ["Working Paper", wp.ref + " — " + wp.label],
      ["Entity", ctx.entityName || ""],
      ["FYE", ctx.fye || ""],
      ["Industry", ctx.industry || ""],
      ["Sector", ctx.sector || ""],
      ["Framework", ctx.framework || ""],
      ["Materiality", "£" + (ctx.materiality || "TBD")],
      ["Performance Materiality", "£" + (ctx.perfMateriality || "TBD")],
      ["Trivial", "£" + (ctx.trivial || "TBD")],
      ["ISA Reference", wp.isa || ""],
      [],
      ["Prepared By", ctx.signOffs?.[wp.id]?.preparedBy || ""],
      ["Reviewed By", ctx.signOffs?.[wp.id]?.reviewedBy || ""],
      ["Generated", new Date().toISOString().slice(0, 10)],
      [],
      ["Notes", ctx.wpNotes?.[wp.id] || ""],
      [],
      ["Generated by AuditEngine v10 AURA"]
    ]
  };
}

/**
 * Extract cell data for a WP prefix into rows.
 */
function extractCellData(cellData, prefix) {
  const keys = Object.keys(cellData).filter(k =>
    k.startsWith(prefix + "_") ||
    k.startsWith("fsli_" + prefix + "_") ||
    k.startsWith("sub_" + prefix + "_") ||
    k.startsWith("proc_" + prefix + "_") ||
    k.startsWith("test_" + prefix + "_") ||
    k.startsWith("cyc_" + prefix + "_")
  );
  if (!keys.length) return null;
  const rows = [["Key", "Value"]];
  keys.forEach(k => rows.push([k, cellData[k]]));
  return { name: "Cell Data", data: rows };
}

/**
 * Build a standardised filename: entityName_FYE_WPRef_YYYYMMDD.ext
 */
function buildFilename(ctx, wpRef, ext) {
  const entity = (ctx.entityName || "Entity").replace(/[^a-zA-Z0-9]/g, "_");
  const fye = (ctx.yearEnd || ctx.fye || "FYE").replace(/[^a-zA-Z0-9]/g, "");
  const ref = (wpRef || "WP").replace(/[^a-zA-Z0-9]/g, "_");
  const date = new Date().toISOString().slice(0, 10).replace(/-/g, "");
  return entity + "_" + fye + "_" + ref + "_" + date + "." + ext;
}

