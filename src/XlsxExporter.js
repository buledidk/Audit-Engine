/**
 * Testing working papers (D1-D16): Cover, Procedures, Sample Grid, Results, Conclusion
 */
export async function exportTestingWP(wp, ctx) {
  const sheets = [coverSheet(ctx, wp)];

  // FSLI Lead sheet
  const fsliLines = ctx.fsliLines || [];
  if (fsliLines.length) {
    const fsliData = [["FSLI Line Item", "PY (£)", "CY (£)", "Movement (£)", "Movement %", "Ref", "Status"]];
    fsliLines.forEach((l, i) => {
      fsliData.push([
        l,
        ctx.getCell ? ctx.getCell("fsli_" + wp.id, i, 1) : "",
        ctx.getCell ? ctx.getCell("fsli_" + wp.id, i, 2) : "",
        "", "", wp.ref, ""
      ]);
    });
    sheets.push({ name: "FSLI Lead", data: fsliData });
  }

  // Procedures sheet
  const tests = [...(ctx.tests?.[wp.id] || []), ...(ctx.customTests?.[wp.id] || [])];
  if (tests.length) {
    const procData = [["Ref", "Procedure", "Assertion", "ISA", "Sample", "Result", "Exceptions", "Judgment", "Judgment Note", "Prepared", "Reviewed"]];
    tests.forEach((t, i) => {
      procData.push([wp.ref + ".S" + String(i + 1).padStart(2, "0"), t, "", wp.isa || "", "", "", "", "", "", "", ""]);
    });
    sheets.push({ name: "Procedures", data: procData });
  }

  // Sample Grid
  const sampleData = [["#", "Item Selected", "Value (£)", "Test Performed", "Result", "Exception", "Comment"]];
  for (let i = 1; i <= 25; i++) sampleData.push([i, "", "", "", "", "", ""]);
  sheets.push({ name: "Sample Grid", data: sampleData });

  // Results
  sheets.push({
    name: "Results",
    data: [
      ["Metric", "Value"],
      ["Total items tested", ""],
      ["Exceptions found", ""],
      ["Exception rate %", ""],
      ["Material exceptions", ""],
      ["Conclusion", ctx.wpNotes?.["conclusion_" + wp.id] || ""],
      ["Follow-up required", ""],
      ["Prepared by", ctx.signOffs?.[wp.id]?.preparedBy || ""],
      ["Reviewed by", ctx.signOffs?.[wp.id]?.reviewedBy || ""],
      ["Date", new Date().toISOString().slice(0, 10)]
    ]
  });

  // Conclusion
  sheets.push({
    name: "Conclusion",
    data: [
      ["Working Paper Conclusion"],
      [],
      ["Notes", ctx.wpNotes?.[wp.id] || ""],
      ["Conclusion", ctx.wpNotes?.["conclusion_" + wp.id] || ""],
    ]
  });

  // Cell data if any
  const cd = extractCellData(ctx.cellData || {}, wp.id);
  if (cd) sheets.push(cd);

  // Evidence Links
  if (ctx.procedureLinks) {
    const evData = [["Procedure Key", "Linked Evidence"]];
    Object.entries(ctx.procedureLinks).forEach(([key, ids]) => { if (ids && ids.length) evData.push([key, ids.join(", ")]); });
    if (evData.length > 1) sheets.push({ name: "Evidence Links", data: evData });
  }

  const filename = buildFilename(ctx, wp.ref, "xlsx");
  await writeWorkbook(filename, sheets);
}

/**
 * Lead schedules (C2-C8): Cover, Schedule with PY/CY/Movement
 */
export async function exportLeadSchedule(wp, ctx) {
  const sheets = [coverSheet(ctx, wp)];

  // Main schedule
  if (ctx.leadHeaders && ctx.leadRows) {
    const data = [ctx.leadHeaders];
    ctx.leadRows.forEach(row => {
      data.push(row.map(cell => {
        if (cell && typeof cell === "object" && cell.props?.children) return String(cell.props.children);
        return cell || "";
      }));
    });
    sheets.push({ name: wp.label.slice(0, 31), data });
  }

  const cd = extractCellData(ctx.cellData || {}, wp.id);
  if (cd) sheets.push(cd);

  const filename = buildFilename(ctx, wp.ref, "xlsx");
  await writeWorkbook(filename, sheets);
}

/**
 * Financial models (FM1-FM8): Cover, Input, Calculation, Output
 */
export async function exportFinancialModel(wp, ctx) {
  const sheets = [coverSheet(ctx, wp)];

  const cd = extractCellData(ctx.cellData || {}, wp.id);
  if (cd) sheets.push(cd);

  // Model-specific data passed via ctx.modelData
  if (ctx.modelData) {
    if (ctx.modelData.inputs) {
      const inputData = [["Input Parameter", "Value"]];
      Object.entries(ctx.modelData.inputs).forEach(([k, v]) => inputData.push([k, v]));
      sheets.push({ name: "Inputs", data: inputData });
    }
    if (ctx.modelData.outputs) {
      const outputData = [["Output", "Value"]];
      Object.entries(ctx.modelData.outputs).forEach(([k, v]) => outputData.push([k, v]));
      sheets.push({ name: "Outputs", data: outputData });
    }
  }

  const filename = buildFilename(ctx, wp.ref, "xlsx");
  await writeWorkbook(filename, sheets);
}

