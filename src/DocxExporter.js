/**
 * A1: Engagement Letter (ISA 210)
 */
export async function generateEngagementLetter(ctx, engagementLetterData) {
  const docx = await loadDocx();
  const el = engagementLetterData;
  const rep = s => s.replace(/\{FYE\}/g, ctx.fye || "[FYE]").replace(/\{FRAMEWORK\}/g, ctx.framework || "[Framework]");
  const hf = buildHeaderFooter(docx, ctx.firmName);

  const children = [
    heading(docx, "ENGAGEMENT LETTER", 1),
    para(docx, ctx.firmName || "[Firm Name]", { bold: true }),
    para(docx, ""),
    para(docx, "To: The Board of Directors of " + (ctx.entityName || "[Entity]"), { bold: true }),
    para(docx, "Date: " + new Date().toISOString().slice(0, 10)),
    para(docx, "Re: Audit of financial statements for the year ended " + (ctx.fye || "[FYE]")),

    heading(docx, "1. Objective (ISA 200.11)", 2),
    para(docx, rep(el.objective)),

    heading(docx, "2. Scope of Audit", 2),
    ...el.scope.map(s => bulletPara(docx, rep(s))),

    heading(docx, "3. Engagement Team", 2),
    simpleTable(docx, ["Role", "Name"], [
      ["Engagement Partner", ctx.partner || "[TBD]"],
      ["Audit Manager", ctx.manager || "[TBD]"],
      ["Audit Firm", ctx.firmName || "[TBD]"],
    ]),

    heading(docx, "4. Responsibilities of Management (ISA 210.6)", 2),
    simpleTable(docx, ["#", "Responsibility"],
      el.responsibilities_management.map((r, i) => [String(i + 1), r])
    ),

    heading(docx, "5. Responsibilities of the Auditor (ISA 210.10)", 2),
    simpleTable(docx, ["#", "Responsibility"],
      el.responsibilities_auditor.map((r, i) => [String(i + 1), r])
    ),

    heading(docx, "6. Inherent Limitations (ISA 200.A53)", 2),
    para(docx, el.limitations || ""),

    heading(docx, "7. Fees", 2),
    para(docx, "[To be agreed]"),
    para(docx, ""),
    para(docx, "Please sign and return one copy of this letter to indicate your acknowledgement of, and agreement with, the arrangements for our audit."),
    para(docx, ""),
    para(docx, "For and on behalf of " + (ctx.firmName || "[Firm]")),
    para(docx, "____________________________"),
    para(docx, ctx.partner || "[Partner]"),
    para(docx, ""),
    para(docx, "For and on behalf of " + (ctx.entityName || "[Entity]")),
    para(docx, "____________________________"),
    para(docx, "[Director Name]"),
  ];

  const doc = new docx.Document({
    sections: [{
      headers: { default: hf.default },
      footers: { default: hf.defaultFooter },
      children,
    }],
  });

  const blob = await docx.Packer.toBlob(doc);
  downloadBlob(blob, makeFilename(ctx.entityName, "A1", "Engagement_Letter"));
}

