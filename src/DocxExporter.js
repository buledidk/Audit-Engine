async function loadDocx() {
  return import("docx");
}

function downloadBlob(blob, filename) {
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = filename;
  a.click();
  URL.revokeObjectURL(a.href);
}

function makeFilename(entityName, ref, suffix) {
  return (entityName || "Entity").replace(/[^a-zA-Z0-9]/g, "_") + "_" + ref + "_" + suffix + ".docx";
}

/**
 * Create a branded header/footer paragraph set.
 */
function buildHeaderFooter(docx, firmName) {
  return {
    default: new docx.Header({
      children: [
        new docx.Paragraph({
          children: [
            new docx.TextRun({ text: firmName || "[Firm Name]", bold: true, size: 16, font: "Calibri" }),
            new docx.TextRun({ text: "  |  CONFIDENTIAL", size: 14, color: "999999", font: "Calibri" }),
          ],
          border: { bottom: { color: "1a237e", size: 6, style: docx.BorderStyle.SINGLE, space: 4 } },
        }),
      ],
    }),
    defaultFooter: new docx.Footer({
      children: [
        new docx.Paragraph({
          children: [
            new docx.TextRun({ text: "Generated by AuditEngine v10 AURA | " + new Date().toISOString().slice(0, 10), size: 14, color: "999999", font: "Calibri" }),
          ],
          alignment: docx.AlignmentType.CENTER,
          border: { top: { color: "cccccc", size: 3, style: docx.BorderStyle.SINGLE, space: 4 } },
        }),
      ],
    }),
  };
}

function heading(docx, text, level) {
  return new docx.Paragraph({
    children: [new docx.TextRun({ text, bold: true, size: level === 1 ? 36 : level === 2 ? 28 : 24, font: "Calibri", color: level === 1 ? "1a237e" : "283593" })],
    spacing: { before: level === 1 ? 400 : 300, after: 150 },
  });
}

function para(docx, text, opts = {}) {
  return new docx.Paragraph({
    children: [new docx.TextRun({ text, size: 22, font: "Calibri", ...opts })],
    spacing: { after: 120 },
  });
}

function bulletPara(docx, text) {
  return new docx.Paragraph({
    children: [new docx.TextRun({ text, size: 22, font: "Calibri" })],
    bullet: { level: 0 },
    spacing: { after: 60 },
  });
}

function simpleTable(docx, headers, rows) {
  return new docx.Table({
    width: { size: 100, type: docx.WidthType.PERCENTAGE },
    rows: [
      new docx.TableRow({
        tableHeader: true,
        children: headers.map(h => new docx.TableCell({
          children: [new docx.Paragraph({ children: [new docx.TextRun({ text: h, bold: true, size: 20, font: "Calibri" })] })],
          shading: { fill: "e8eaf6" },
          verticalAlign: docx.VerticalAlign.CENTER,
        })),
      }),
      ...rows.map(row => new docx.TableRow({
        children: row.map(cell => new docx.TableCell({
          children: [new docx.Paragraph({ children: [new docx.TextRun({ text: String(cell || ""), size: 20, font: "Calibri" })] })],
          verticalAlign: docx.VerticalAlign.CENTER,
        })),
      })),
    ],
  });
}

